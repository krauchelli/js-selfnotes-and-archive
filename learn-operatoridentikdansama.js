const nilai = 10;
const value = "10";

//operator == (sama)
console.log(nilai == value);
//operator === (identik)
console.log(nilai === value);

/*
    pada kasus ini, identik dalam pengertiannya berada dalam range kesamaan tipe data, sedangkan "sama" dalam kasus ini
    tidak melihat tipe data tersebut berbeda atau tidak.
*/